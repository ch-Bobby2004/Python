# Python has several numeric types:
a = 10        # int
b = 3.14      # float
print(type(a), type(b))


# Strings (str)
# A string is text, enclosed in single, double, or triple quotes.

name = "Bobby"
message = 'Hello'
multiline = """This is
a multiline string"""
print(name, message, multiline)

# Sure! In Python, triple quotes """ (or ''') are used to create multiline strings. This allows the string to span multiple lines without using special characters like \n


# Boolean (bool)
# Boolean type stores True or False.

is_student = True
has_passed = False
print(is_student, has_passed)